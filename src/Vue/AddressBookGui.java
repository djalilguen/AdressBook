    private void setTextFields() {
        saisie.add(lNameTxt);
        saisie.add(fNameTxt);
        saisie.add(mNameTxt);
        saisie.add(phoneTxt);
        saisie.add(emailTxt);
        saisie.add(adress1Txt);
        saisie.add(adress2Txt);
        saisie.add(cityTxt);
        saisie.add(stateTxt);
        saisie.add(countryTxt);
        saisie.add(zipTxt);
    }

    private void effacerSaisie() {
        for (JTextField tmp : saisie) {
            tmp.setText("");
        }
    }

    private void activerSaisie() {
        for (JTextField tmp : saisie) {
            tmp.setEditable(true);
        }
    }

    private void desactiverSaisie() {

        for (JTextField tmp : saisie) {
            tmp.setEditable(false);
        }
    }

    private void desactiverBtn() {
        editBtn.setEnabled(false);
        saveBtn.setEnabled(false);
        cancelBtn.setEnabled(false);
        deleteBtn.setEnabled(false);
    }

    private void optionsNewBtn() {
        activerSaisie();
        effacerSaisie();
        saveBtn.setEnabled(true);
        cancelBtn.setEnabled(true);
        deleteBtn.setEnabled(false);
        editBtn.setEnabled(false);
        newBtn.setEnabled(false);
        lNameTxt.requestFocusInWindow();
    }

    private void optionsEditBtn() {
        saveBtn.setEnabled(true);
        cancelBtn.setEnabled(true);
        activerSaisie();
        lNameTxt.requestFocusInWindow();
    }

    private void optionDeleteBtn() {
        deleteBtn.setEnabled(false);
        editBtn.setEnabled(false);
        cancelBtn.setEnabled(false);
        lNameTxt.requestFocusInWindow();
    }

    private void optionsSelectContact() {
        editBtn.setEnabled(true);
        deleteBtn.setEnabled(true);
        saveBtn.setEnabled(false);
        cancelBtn.setEnabled(false);
        desactiverSaisie();
    }

    private void optionSaveBtn() {
        desactiverSaisie();
        newBtn.setEnabled(true);
        saveBtn.setEnabled(false);
        cancelBtn.setEnabled(false);
//            listContact.requestFocusInWindow();
    }

    private void optionCancelBtn() {
        desactiverSaisie();
        effacerSaisie();
        newBtn.setEnabled(true);
        saveBtn.setEnabled(false);
        editBtn.setEnabled(false);
        deleteBtn.setEnabled(false);
        listContact.requestFocusInWindow();
    }

    private void remplirContact() {
        contact.setLastName(lNameTxt.getText());
        contact.setFirstName(fNameTxt.getText());
        contact.setMiddleName(mNameTxt.getText());
        contact.setPhone(phoneTxt.getText());
        contact.setEmail(emailTxt.getText());
        contact.setAddress1(adress1Txt.getText());
        contact.setAddress2(adress2Txt.getText());
        contact.setCity(cityTxt.getText());
        contact.setState(stateTxt.getText());
        contact.setCountry(countryTxt.getText());
        contact.setZip(zipTxt.getText());

    }

    /*La fonction verifierCampsvides permet de valider les champs de saisie obligatoires*/
    private boolean verifierChampsVides() {

        if ((lNameTxt.getText().length() > 0) && (fNameTxt.getText().length() > 0)
                && (phoneTxt.getText().length() > 0) && (adress1Txt.getText().length() > 0)
                && (cityTxt.getText().length() > 0) && (stateTxt.getText().length() > 0)
                && (countryTxt.getText().length() > 0) && (zipTxt.getText().length() > 0)) {
            return true;
        } else {
            return false;
        }
    }

    private static void traiterExceptionDoublon(ContactDejaPresentException e) {
        JOptionPane.showMessageDialog(null,
                "Erreur insertion!, Contact déjà présent\n" + e.getContact(),
                "Ajout non complété", JOptionPane.ERROR_MESSAGE);
    }

    private static void AjoutSuccesMessage(Contact c) {
        
        String infoContact = "Nom: " + c.getFullName()+ "\n";
            JOptionPane.showMessageDialog(null,
                    "Contact a été ajouté avec succès !\n" + infoContact,
                    "Ajout complété", JOptionPane.INFORMATION_MESSAGE);
    }


