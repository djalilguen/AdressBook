    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        // enregistrer les modifications dans le contact
        if (verifierChampsVides()) {
            if (contact.getLastName() == null) {
                // System.out.println("null");
                try {
                    remplirContact();
                    book.ajouterContact(contact);
                    AjoutSuccesMessage(contact);
                } catch (ContactDejaPresentException e) {
                    traiterExceptionDoublon(e);
                }

            } else {
                remplirContact();
            }
            //book.modifierContact(listContact.getSelectedIndex(),contact);
            ManipulerFichier.remplirList(listContact, book);

            optionSaveBtn();

        } else {
            JOptionPane.showMessageDialog(null,
                    "Les champs marqués par (*) sont obligatoire", "Erreur de saisie",
                    JOptionPane.ERROR_MESSAGE);
            //listContact.requestFocusInWindow();
        }
        listContact.requestFocusInWindow();
    }//GEN-LAST:event_saveBtnActionPerformed

